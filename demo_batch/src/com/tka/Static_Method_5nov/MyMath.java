package com.tka.Static_Method_5nov;

public class MyMath {
public static void addition(int a, int b) {
	int c= a+b;
	System.out.println("Addition of a & b: "+c);
}

public static void substraction(int a1, int b1) {
	int c=a1-b1;
	System.out.println(c);
}

public static void multiplication(int a2, int b2) {
	int c=a2*b2;
	System.out.println(c);
}

public static void division(int a3, int b3) {
	int c=a3/b3;
	System.out.println(c);
}

public static void modulo(int a4, int b4) {
	int c=a4%b4;
	System.out.println(c);
}
}



